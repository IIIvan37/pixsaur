BUILDSNA
BANKSET 0

;------------------------------------------------------------------------------
; Gate Array and RAM/ROM/CRTC setup
;------------------------------------------------------------------------------

gate_array:     equ #7f00

penr:           equ %00000000
inkr:           equ %01000000
rmr:            equ %10000000

ram_p0:         equ #0000
ram_p1:         equ #4000
ram_p2:         equ #8000
ram_p3:         equ #c000

upper_off:      equ %00001000
upper_on:       equ %00000000
lower_off:      equ %00000100
lower_on:       equ %00000000
rom_off:        equ upper_off | lower_off

clear_raster_div: equ %00010000

macro set_mode mode
    ld      bc, gate_array | rmr | rom_off | {mode}
    out     (c), c
    ; print "Mode :", {hex}gate_array | rmr | rom_off | {mode}
endm

;------------------------------------------------------------------------------
; Program entry point
;------------------------------------------------------------------------------

        org     #b000
        run     #b000

        di
        ld      sp, #0038

        set_mode 0

        ld      hl, palette_hardware
        ld      d, 15
        call    setPalette

        ld      hl, tovercrt         ; Switch CRTC to 92 columns, 264 lines
        call    outcrtc              ; with #d0 as start address

        call    affscr               ; Display the screen

end_loop:
        jr      end_loop

;------------------------------------------------------------------------------
; Display screen routine
;------------------------------------------------------------------------------

affscr:
        ld      b, #ff               ; 264 lines doesn't fit in 8 bits!
        ld      c, #60               ; Process in two passes
        ld      de, #00d0            ; Screen address
        ld      hl, #4268            ; Overscan area address
        call    .bclt1               ; Transfer 255 lines
        ld      b, #09               ; Then the remaining 9 lines

.bclt1:
        push    de                   ; Save screen address
        push    bc
        ld      b, #00
        ldir                        ; Transfer 1 line
        pop     bc
        pop     de

        push    hl
        ex      de, hl
        call    adinfuni
        ld      a, h
        or      a
        jr      nz, .okaff
        ld      h, #40
.okaff:
        ex      de, hl
        pop     hl
        djnz    .bclt1
        ret

;------------------------------------------------------------------------------
; Output to CRTC
;------------------------------------------------------------------------------

outcrtc:
        ld      bc, #bc00            ; Activate CRTC

bcloutc:
        ld      a, (hl)
        cp      #ff
        ret     z
        out     (c), c               ; Select port #bc00 to #bc12
        inc     b
        out     (c), a               ; Send byte to port #bdxx
        dec     b
        inc     c
        inc     hl
        jr      bcloutc

;------------------------------------------------------------------------------
; Special ADINF routine
;------------------------------------------------------------------------------

adinfuni:
        ld      a, h
        add     a, #08
        ld      h, a
        and     #38
        ret     nz
        ld      a, h
        sub     #40
        ld      h, a
        ld      a, l
        add     a, #5c
        ld      l, a
        ret     nc
        inc     h
        ld      a, h
        and     #07
        ret     nz
        ld      a, h
        sub     #08
        ld      h, a
        ret

;------------------------------------------------------------------------------
; Set hardware palette
; input: a = first ink, c = palette length, hl = palette address
; destroys: af, bc, hl
;------------------------------------------------------------------------------

setPalette:
        ld      b, hi(gate_array)
.loop:
        out     (c), a
        inc     b
        outi
        inc     a
        dec     c
        jr      nz, .loop
        ret

;------------------------------------------------------------------------------
; CRTC register values for overscan
;------------------------------------------------------------------------------

tovercrt:
        db      #3e, #2e, #30, #0e, #26, #00, #20, #22
        db      #00, #07, #00, #00, #0c, #68, #ff

palette_hardware:
        db      #54, #5f, #53, #45

        org     #4268

